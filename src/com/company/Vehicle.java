package com.company;

public class Vehicle {
    String brand;
    String model;
    int number_of_wheels;
    int max_speed;
    public Vehicle(String brand, String model, int number_of_wheels, int max_speed){
        this.brand = brand;
        this.model = model;
        this.number_of_wheels = number_of_wheels;
        this.max_speed = max_speed;
    }
}


